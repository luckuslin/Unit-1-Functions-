import turtle
from turtle import *
t = Turtle()

t.shape('turtle')

def square(x):
    t.forward(x)
    t.left(90)
    t.forward(x)
    t.left(90)
    t.forward(x)
    t.left(90)
    t.forward(x)
    t.left(90)
def squa(x):
    t.forward(x)
    t.left(90)
    t.forward(x)
    t.left(90)
    t.forward(x)
    t.left(90)
square(100)
squa(200)
square(400)
squa(300)
squa(200)
square(100)
turtle.done()
